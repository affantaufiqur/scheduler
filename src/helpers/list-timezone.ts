/**
 * Type for time zone keys
 */
export type TimeZoneKey = keyof typeof TIME_ZONES;

/**
 * Type for time zone values
 */
export type TimeZoneValue = (typeof TIME_ZONES)[TimeZoneKey];

/**
 * Get all time zone keys
 */
export function getTimeZoneKeys(): TimeZoneKey[] {
  return Object.keys(TIME_ZONES) as TimeZoneKey[];
}

/**
 * Get all time zone values
 */
export function getTimeZoneValues(): TimeZoneValue[] {
  return Object.values(TIME_ZONES);
}

/**
 * Get a time zone by key
 */
export function getTimeZone(key: TimeZoneKey): TimeZoneValue {
  return TIME_ZONES[key];
}

/**
 * Check if a key exists
 */
export function hasTimeZone(key: string): key is TimeZoneKey {
  return key in TIME_ZONES;
}

export type AllTimeZones = TimeZoneKey; // This gives you a union of all time zone keys
